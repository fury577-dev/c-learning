#include <stdio.h>

int main() {

    int number = 10;

    // Check if number is positive, negative, or zero
    if (number > 0) {
        printf("Number is positive\n");
    } 
    else if (number < 0) {
        printf("Number is negative\n");
    } 
    else {
        printf("Number is zero\n");
    }

    return 0;
}
